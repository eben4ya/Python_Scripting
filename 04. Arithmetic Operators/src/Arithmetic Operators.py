# OPERATOR ARITMATIKA
a = 9
b = 2

# Operasi Penjumlahan +
hasil = a + b
print(a, '+', b, '=' , hasil)

# Operasi Pengurangan -
hasil = a - b
print(a, '-', b, '=' , hasil)

# Operasi Perkalian *
hasil = a * b
print(a, 'x', b, '=' , hasil)

# Operasi Pembagian /
hasil = a / b
print(a, '/', b, '=' , hasil)

# Operasi Eksponen (pangkat)
hasil = a ** b
print(a, '^', b, '=' , hasil)

# Operasi Modulus %
hasil = a % b
print(a, '%', b, '=' , hasil)

# Operasi Floor Division // (inverse modulus)
hasil = a // b
print(a, '//', b, '=' , hasil)

